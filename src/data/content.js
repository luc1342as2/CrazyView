// Get similar content by shared genres (excludes current item)
export function getSimilarContent(content, limit = 12) {
  if (!content?.genre?.length) return [];
  const ids = Object.keys(contentCatalog).map(Number);
  const scored = ids
    .filter((id) => id !== content.id)
    .map((id) => {
      const item = contentCatalog[id];
      const sharedGenres = item?.genre?.filter((g) => content.genre.includes(g)).length || 0;
      return { item, score: sharedGenres };
    })
    .filter(({ score }) => score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, limit);
  return scored.map(({ item }) => item);
}
